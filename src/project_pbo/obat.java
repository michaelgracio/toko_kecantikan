    private void tableObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableObatMouseClicked
        // TODO add your handling code here:
        int baris = tableObat.rowAtPoint(evt.getPoint());
        String kode_obat =tableObat.getValueAt(baris, 1).toString();
        tfKodeObat.setText(kode_obat);
        String merek_obat = tableObat.getValueAt(baris,2).toString();
        tfMObat.setText(merek_obat);
        String nama_obat = tableObat.getValueAt(baris,3).toString();
        tfNamaObat.setText(nama_obat);
        String jenis = tableObat.getValueAt(baris, 4).toString();
        tfJenisObat.setText(jenis);
        String stock=tableObat.getValueAt(baris, 5).toString();
        tfStock.setText(stock);
        String biaya=tableObat.getValueAt(baris, 6).toString();
        tfBiaya.setText(biaya);
    }//GEN-LAST:event_tableObatMouseClicked


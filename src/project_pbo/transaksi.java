    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String kode_transaksi =jTable1.getValueAt(baris, 1).toString();
        tfKodeTrans.setText(kode_transaksi);
        String id_pembeli = jTable1.getValueAt(baris,2).toString();
        tfIDpembeli.setText(id_pembeli);
        String kode_obat = jTable1.getValueAt(baris, 3).toString();
        cbKodeObat.setSelectedItem(kode_obat);
        String jumlah_beli=jTable1.getValueAt(baris, 4).toString();
        tfJumlahBeli.setText(jumlah_beli);
        String biaya=jTable1.getValueAt(baris, 5).toString();
        tfBiaya.setText(biaya);
        String total_biaya=jTable1.getValueAt(baris, 6).toString();
        tfTotBay.setText(total_biaya);
        String tanggal_transaksi=jTable1.getValueAt(baris, 7).toString();
        tfTanggal.setText(tanggal_transaksi);
        String metode_bayar = jTable1.getValueAt(baris, 8).toString();
        cbMetodeBayar.setSelectedItem(metode_bayar);
    }//GEN-LAST:event_jTable1MouseClicked


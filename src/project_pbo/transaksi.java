    private void cbKodeObatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbKodeObatActionPerformed
        // TODO add your handling code here:
        try{
            
            int no = 1;
            String sql = "SELECT biaya from obat where kode_obat = '"+cbKodeObat.getSelectedItem()+"'";
            java.sql.Connection conn = (Connection)konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()){
            tfBiaya.setText(res.getString("biaya"));    
            }
            
            
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_cbKodeObatActionPerformed

